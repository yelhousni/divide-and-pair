package fp

// ExpByOcticExp is equivalent to z.Exp(x, e7db4ea6533afa906673b0101343b00aa77b4805fffcb7fdfffffffe0000000).
// It raises x to the (p-1)/8 power using a 295-step addition chain.
//
// uses github.com/mmcloughlin/addchain v0.4.0 to generate the addition chain
func (z *Element) ExpByOcticExp(x Element) *Element {
	// addition chain:
	//
	//	_10       = 2*1
	//	_11       = 1 + _10
	//	_100      = 1 + _11
	//	_110      = _10 + _100
	//	_1100     = 2*_110
	//	_10010    = _110 + _1100
	//	_10011    = 1 + _10010
	//	_10110    = _11 + _10011
	//	_11000    = _10 + _10110
	//	_11010    = _10 + _11000
	//	_100010   = _1100 + _10110
	//	_110101   = _10011 + _100010
	//	_111011   = _110 + _110101
	//	_1001011  = _10110 + _110101
	//	_1001101  = _10 + _1001011
	//	_1010101  = _11010 + _111011
	//	_1100111  = _10010 + _1010101
	//	_1101001  = _10 + _1100111
	//	_10000011 = _11010 + _1101001
	//	_10011001 = _10110 + _10000011
	//	_10011101 = _100 + _10011001
	//	_10111111 = _100010 + _10011101
	//	_11010111 = _11000 + _10111111
	//	_11011011 = _100 + _11010111
	//	_11100111 = _1100 + _11011011
	//	_11101111 = _11000 + _11010111
	//	_11111111 = _11000 + _11100111
	//	i55       = ((_11100111 << 8 + _11011011) << 9 + _10011101) << 9
	//	i75       = ((_10011001 + i55) << 9 + _10011001) << 8 + _11010111
	//	i102      = ((i75 << 6 + _110101) << 10 + _10000011) << 9
	//	i121      = ((_1100111 + i102) << 8 + _111011) << 8 + 1
	//	i162      = ((i121 << 14 + _1001101) << 10 + _111011) << 15
	//	i183      = ((_1010101 + i162) << 10 + _11101111) << 8 + _1101001
	//	i216      = ((i183 << 16 + _10111111) << 8 + _11111111) << 7
	//	i236      = ((_1001011 + i216) << 9 + _11111111) << 8 + _10111111
	//	i262      = ((i236 << 8 + _11111111) << 8 + _11111111) << 8
	//	return      ((_11111111 + i262) << 2 + _11) << 29
	//
	var t [27]Element

	// t[0] = x^0x2
	t[0].Square(&x)
	// t[1] = x^0x3
	t[1].Mul(&x, &t[0])
	// t[2] = x^0x4
	t[2].Mul(&x, &t[1])
	// t[3] = x^0x6
	t[3].Mul(&t[0], &t[2])
	// t[4] = x^0xc
	t[4].Square(&t[3])
	// t[5] = x^0x12
	t[5].Mul(&t[3], &t[4])
	// t[6] = x^0x13
	t[6].Mul(&x, &t[5])
	// t[7] = x^0x16
	t[7].Mul(&t[1], &t[6])
	// t[8] = x^0x18
	t[8].Mul(&t[0], &t[7])
	// t[9] = x^0x1a
	t[9].Mul(&t[0], &t[8])
	// t[10] = x^0x22
	t[10].Mul(&t[4], &t[7])
	// t[11] = x^0x35
	t[11].Mul(&t[6], &t[10])
	// t[12] = x^0x3b
	t[12].Mul(&t[3], &t[11])
	// t[13] = x^0x4b
	t[13].Mul(&t[7], &t[11])
	// t[14] = x^0x4d
	t[14].Mul(&t[0], &t[13])
	// t[15] = x^0x55
	t[15].Mul(&t[9], &t[12])
	// t[16] = x^0x67
	t[16].Mul(&t[5], &t[15])
	// t[17] = x^0x69
	t[17].Mul(&t[0], &t[16])
	// t[18] = x^0x83
	t[18].Mul(&t[9], &t[17])
	// t[19] = x^0x99
	t[19].Mul(&t[7], &t[18])
	// t[20] = x^0x9d
	t[20].Mul(&t[2], &t[19])
	// t[21] = x^0xbf
	t[21].Mul(&t[10], &t[20])
	// t[22] = x^0xd7
	t[22].Mul(&t[8], &t[21])
	// t[23] = x^0xdb
	t[23].Mul(&t[2], &t[22])
	// t[24] = x^0xe7
	t[24].Mul(&t[4], &t[23])
	// t[25] = x^0xef
	t[25].Mul(&t[8], &t[22])
	// t[26] = x^0xff
	t[26].Mul(&t[8], &t[24])

	// i55 = ((_11100111 << 8 + _11011011) << 9 + _10011101) << 9
	z.nSquare(&t[24], 8)
	z.Mul(z, &t[23])
	z.nSquare(z, 9)
	z.Mul(z, &t[20])
	z.nSquare(z, 9)

	// i75 = ((_10011001 + i55) << 9 + _10011001) << 8 + _11010111
	z.Mul(z, &t[19])
	z.nSquare(z, 9)
	z.Mul(z, &t[19])
	z.nSquare(z, 8)
	z.Mul(z, &t[22])

	// i102 = ((i75 << 6 + _110101) << 10 + _10000011) << 9
	z.nSquare(z, 6)
	z.Mul(z, &t[11])
	z.nSquare(z, 10)
	z.Mul(z, &t[18])
	z.nSquare(z, 9)

	// i121 = ((_1100111 + i102) << 8 + _111011) << 8 + 1
	z.Mul(z, &t[16])
	z.nSquare(z, 8)
	z.Mul(z, &t[12])
	z.nSquare(z, 8)
	z.Mul(z, &x)

	// i162 = ((i121 << 14 + _1001101) << 10 + _111011) << 15
	z.nSquare(z, 14)
	z.Mul(z, &t[14])
	z.nSquare(z, 10)
	z.Mul(z, &t[12])
	z.nSquare(z, 15)

	// i183 = ((_1010101 + i162) << 10 + _11101111) << 8 + _1101001
	z.Mul(z, &t[15])
	z.nSquare(z, 10)
	z.Mul(z, &t[25])
	z.nSquare(z, 8)
	z.Mul(z, &t[17])

	// i216 = ((i183 << 16 + _10111111) << 8 + _11111111) << 7
	z.nSquare(z, 16)
	z.Mul(z, &t[21])
	z.nSquare(z, 8)
	z.Mul(z, &t[26])
	z.nSquare(z, 7)

	// i236 = ((_1001011 + i216) << 9 + _11111111) << 8 + _10111111
	z.Mul(z, &t[13])
	z.nSquare(z, 9)
	z.Mul(z, &t[26])
	z.nSquare(z, 8)
	z.Mul(z, &t[21])

	// i262 = ((i236 << 8 + _11111111) << 8 + _11111111) << 8
	z.nSquare(z, 8)
	z.Mul(z, &t[26])
	z.nSquare(z, 8)
	z.Mul(z, &t[26])
	z.nSquare(z, 8)

	// return ((_11111111 + i262) << 2 + _11) << 29
	z.Mul(z, &t[26])
	z.nSquare(z, 2)
	z.Mul(z, &t[1])
	z.nSquare(z, 29)

	return z
}

func (z *Element) nSquare(x *Element, n int) *Element {
	z.Set(x)
	for i := 0; i < n; i++ {
		z.Square(z)
	}
	return z
}
